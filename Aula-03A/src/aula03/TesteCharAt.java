package aula03;

public class TesteCharAt {

	public static void main(String[] args) {
		
		// O metodo charAt retorna o caractere
		// em uma determinada posicao da String
		// indicada como argumento do metodo.
		
		String cidade = "Feliz";
		System.out.println(cidade.charAt(0));
		System.out.println(cidade.charAt(2));
		System.out.println(cidade.charAt(4));
	}

}
