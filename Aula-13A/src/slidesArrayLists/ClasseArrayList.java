package slidesArrayLists;

public class ClasseArrayList {

	public static void main(String[] args) {
		
		/*
		 * O Java fornece várias estruturas de dados 
		 * predefindias, chamadas coleções, usadas para 
		 * armazenar grupos de objetos relacionados.
		 * 
		 * Como exemplo, temos a classe ArrayList.
		 * 
		 * Os arrays que estudamos até agora não alteram
		 * automaticamente o seu tamanho em tempo de
		 * execução para acomodar elementos adicionais.
		 * 
		 * A classe de coleção ArrayList<T> fornece uma 
		 * solução conveniente para este problema. Nesta 
		 * classe, o T é um espaço reservado que indica o 
		 * tipo de elemento que vamos ter no ArrayList.
		 * 
		 * Exemplos:
		 * 
		 * ArrayList<String> lista1;
		 * ArrayList<int> numeros;
		 * ArrayList<double> listaDePrecos;
		 * 
		 * 
		 * Métodos da classe ArrayList:
		 * 		- add: para adicionar um elemento;
		 * 		- clear: para remover todos elementos;
		 * 		- get: para retornar um elemento específico;
		 * 		- remove: para remover elementos;
		 * 		- size: para obter o número de elementos;
		 * 		- contains: para verificar se um objeto está contido no array.
		 * 
		 * Exemplos:
		 * [Classe TesteAddSizeEGet]
		 * [Classe TesteClear]
		 * [Classe TesteRemove]
		 * 
		 * 
		 * Comparativo Array e ArrayList:
		 * 
		 * 	arrays (vetores):
		 * 		- Capacidade fixa;
		 * 		- Acessado com z[i];
		 * 		- Construtor: 	new double[10];
		 * 						new int[20];
		 * 		- Obtemos seu tamanho com z.length;
		 * 		- Não possui métodos;
		 * 		- Mais rápido e menos flexível.
		 * 
		 * 	ArrayList:
		 * 		- Capacidade aumenta a medida que novos elementos são adicionados;
		 * 		- Acessado com z.get(i);
		 * 		- Construtor:	new ArrayList<Classe>();
		 * 		- Muitos métodos:	z.size();
		 * 							z.add();
		 * 							z.get(); etc.
		 * 		- Mais lento e mais flexível.
		 * 
		 */

	}

}
