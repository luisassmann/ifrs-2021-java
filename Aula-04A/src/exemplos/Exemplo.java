package exemplos;

public class Exemplo {

	public static void main(String[] args) {
		
		/* 
		 * Ate agora os programas foram estruturados em
		 * um unico arquivo, em uma única classe.
		 * Esses programas costumam apresentar dificuldades
		 * a medida que eles vao crescendo, onde o mais facil
		 * seria modularizar para maior eficiencia.
		*/
		
		float f = 1.5f;
		double d = 2.7;
		int i1 = (int) f;
		int i2 = (int) d;
		
		System.out.println(f);
		System.out.println(d);
		System.out.println(i1);
		System.out.println(i2);

	}

}
