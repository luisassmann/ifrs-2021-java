package aula;

public class OrientacaoObjetos {
	/*
	 * A Programação Orientada a objetos é o Paradigma no qual
	 * um programa é estruturado.
	 * 
	 * Permite que objetos do mundo real (carros, filmes, bicicletas,
	 * celulares, etc.) que possuem características semelhantes, sejam
	 * abstraídos e mapeados em objetos no programa de computador.
	 * 
	 * Objeto é uma entidade que combina estrutura de dados
	 * e comportamento funcional.
	 * 
	 * -- Classe --
	 * 
	 * Uma Classe representa um tipo abstrato de dado. As classes usadas
	 * para representar objetos complexos que têm estado e
	 * comportamento.
	 * 
	 * Uma Classe é um gabarito para definição de objetos que possui:
	 * 
	 * - Atributos: são propriedades da classe que armazena o seu estado;
	 * 
	 * - Métodos: são funcionalidades (comportamento) que permitem
	 *   a modificação do seu estado.
	 * 
	 * Diagrama de classes:
	 * |-----------------|
	 * |     Carro       |
	 * |-----------------|
	 * | +modelo: String |
	 * | +marca: String  |
	 * |-----------------|
	 * | +andar(): void  |
	 * |-----------------|
	 * 
	 * Representação em Código Java no arquivo Carro.java
	 * 
	 * O nome da classe SEMPRE começa com letra maiúscula!
	 * 
	 * --- Atributo ---
	 * 
	 * Os atributos definem propriedades/características que o objeto possui.
	 * Um atributo é identificado por um nome e um tipo.
	 * 
	 * Os atributos podem opcionalmente estar inicializados, conforme
	 * o exemplo da classe Carro (Carro.java). No exemplo da Pessoa (Pessoa.java)
	 * não há inicialização.
	 * 
	 * O nome de um atributo SEMPRE deve estar em letra minúscula!
	 * 
	 * ---- Método ----
	 * 
	 * Os métodos definem as ações (funcionalidades) da classe, ou seja, o que os
	 * objetos fazem.
	 * 
	 * Um método é composto por:
	 * 
	 * - um identificador para o método (o nome do método);
	 * - o tipo para o valor de retorno;
	 * - e opcionalmente por sua lista de argumentos, sendo cada argumento
	 *   identificado por seu tipo e nome.
	 * 
	 * O nome do método SEMPRE deve começar com letra minúscula!
	 * 
	 * Exemplos de Métodos na classe Pessoa. (Pessoa.java)
	 * 
	 * ----- Escopo -----
	 * 
	 * Os atributos são acessíveis em qualquer lugar da classe, mas variáveis
	 * criadas dentro de um método são inacessíveis fora deste. (Carro.java)
	 * 
	 * ------ Objeto ------
	 * 
	 * Objetos são instâncias de classe. Quando instanciamos um objeto
	 * de uma classe, estamos criando um novo item do conjunto representado
	 * por essa classe.
	 * 
	 * Cada Objeto possui:
	 * 
	 * - Estado: objetos diferentes podem ter valores diferentes para seus atributos;
	 * - Comportamento: definido pelos métodos, que são comuns a todos objetos da mesma classe.
	 * 
	 * Exemplos:
	 * 
	 * - Livro: O Mundo de Sofia, Tutorial Java, ...
	 * - Filme: Matrix, Sexto Sentido, Avatar, Blade Runner, ...
	 * - Carro: Civic, Fiesta, S10, Corolla, HRV, ...
	 * 
	 */
}
