package exemplos;

public class IncrementoDecremento {

	public static void main(String[] args) {
		
		int i = 10;
		i++;
		i+=2;
		System.out.println(i);
		
		int j = 100;
		j--;
		j-=3;
		System.out.println(j);
		
		int k = 2;
		k *= 2;
		System.out.println(k);
		
		int h = 10;
		h /= 2;
		System.out.println(h);
		
	}

}
