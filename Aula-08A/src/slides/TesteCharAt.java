package slides;

public class TesteCharAt {

	public static void main(String[] args) {
		
		// O metodo charAt retorna o caractere de 
		// determinada posicao da String, indicado como argumento do metodo.
		
		String cidade = "Feliz";
		
		System.out.println(cidade.charAt(0));
		System.out.println(cidade.charAt(2));
		System.out.println(cidade.charAt(4));

	}

}
