package slides;

public class Interfaces {

	/*
	 * Uma interface é considerada uma entidade.
	 * 
	 * Para criar uma interface usamos a palavra-chave
	 * interface no lugar da palavra-chave class.
	 * 
	 * Uma interface possui apenas assinaturas de métodos,
	 * públicos e abstratos (sem corpo). Não pode possuir
	 * métodos concretos (com corpo), nem métodos estáticos.
	 * 
	 * Os prefixos abstract e public podem ser omitidos.
	 * 
	 * Uma interface não é instanciável, ou seja, não podemos
	 * criar objetos de uma interface usando o operador new.
	 * 
	 * Uma interface não pode conter variáveis de instância.
	 * 
	 * Mas uma interface pode conter constantes.
	 * 
	 * Exemplo arquivo IPlayer::
	 * 
	 * Para se utilizar uma interface, usamos a palavra-chave
	 * implements na declaração de uma classe que vai
	 * implementar a interface.
	 * 
	 * Exemplo arquivo Player::
	 * 
	 * Nas classes que implementam a Interface, devemos criar
	 * o corpo dos métodos que especificamos na interface.
	 * 
	 * CLASSE ABSTRATA X INTERFACE
	 * 
	 * Uma classe abstrata pode conter métodos completos e abstratos,
	 * e uma interface pode conter apenas assinaturas de métodos.
	 * 
	 * Uma classe abstrata pode conter atributos e construtores.
	 * Uma interface não pode conter atributos nem construtores,
	 * somente constantes.
	 * 
	 * Uma classe abstrata não suporta herança múltipla, porém
	 * uma classe pode implementar várias interfaces.
	 */

}
