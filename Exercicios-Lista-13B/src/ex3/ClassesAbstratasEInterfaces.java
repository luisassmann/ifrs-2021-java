package ex3;

public class ClassesAbstratasEInterfaces {
	/*
	 * Com as Interfaces é impossível criar métodos concretos,
	 * o que pode ser feito em Classes Abstratas.
	 * 
	 * Nas interfaces só é possível fazer a assinatura de métodos e definição de constantes,
	 * sem código nos métodos e sem variáveis comuns, tendo uma diferença
	 * em relação às Classes Abstratas, onde é possível criar variáveis
	 * e métodos com códigos.
	 * 
	 * Para se utilizar as Interfaces, utiliza-se a palavra-chave
	 * implements do lado da classe em que a interface for ser
	 * utilizada.
	 * 
	 * As classes abstratas são utilizadas com a palavra-chave extends.
	 * 
	 * Classes Abstratas não suportam heranças múltiplas.
	 * Porém uma classe pode implementar várias interfaces.
	 * 
	 * Interfaces não podem conter atributos e nem construtores,
	 * diferentemente das classes abstratas.
	 */
}
